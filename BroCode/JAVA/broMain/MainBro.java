package broMain;


public class MainBro {
    /**
     * ^ Shortcut: class
     * 
     * This is a class: a "blueprint" of sorts for creating objects. It shares it's
     * name with the Java file.
     */

    public static void main(String[] args) {
        /**
         * ^ Shortcut: main
         * 
         * This is the Main method. It must be defined in order for the program to run.
         */

        System.out.println("Bro!");
        /**
         * ^ Shortcut: "sout".
         * 
         * "print" prints text.
         * 
         * "println" prints onto a new line. \n also does this.
         * 
         * Using \ adds an escape sequence, which is used to bypass a character's
         * specific action.
         * 
         * \t tabs
         * 
         * \" adds a quotation mark
         * 
         */
    }
}

